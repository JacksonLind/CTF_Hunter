"""
Attack Plan tab for CTF Hunter.

Displays hypotheses generated by the HypothesisEngine ordered by confidence.
Each hypothesis renders as an expandable card showing:
  - Category badge and confidence bar
  - Present / missing findings lists
  - Suggested commands as copy-to-clipboard code blocks with "Run Command" buttons
  - Transform pipeline steps
  - Source tag ("rules" vs "ai")

A "Generate Exploit" button appears for pwn-category hypotheses with
confidence ≥ ExploitGenerator.MIN_CONFIDENCE.
"""
