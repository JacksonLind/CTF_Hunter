_DEFAULT_COLOURS = ("#333333", "#f8f8f8")


def _category_colours(cat: str) -> tuple[str, str]:
    return _CATEGORY_COLOURS.get(cat.lower(), _DEFAULT_COLOURS)


# ---------------------------------------------------------------------------
# Command block widget (code + copy + run buttons)
# ---------------------------------------------------------------------------

class _CommandBlock(QWidget):
    """A single command line shown as a code block with Copy and Run buttons."""

    run_requested = pyqtSignal(str)   # emitted with the raw command string

    def __init__(self, command: str, parent: Optional[QWidget] = None) -> None:
        super().__init__(parent)
        self._command = command

        layout = QHBoxLayout(self)
        layout.setContentsMargins(0, 2, 0, 2)
        layout.setSpacing(4)

        # Code label (mono font, light background)
        self._label = QLabel(command)
        self._label.setFont(QFont("Courier", 9))
        self._label.setStyleSheet(
            "background:#1e1e1e; color:#d4d4d4; padding:4px 6px; border-radius:3px;"
        )
        self._label.setWordWrap(True)
        self._label.setSizePolicy(QSizePolicy.Policy.Expanding, QSizePolicy.Policy.Preferred)
        layout.addWidget(self._label, 1)

        # Copy button
        copy_btn = QPushButton("📋")
        copy_btn.setFixedSize(QSize(28, 28))
        copy_btn.setToolTip("Copy command to clipboard")
        copy_btn.clicked.connect(self._copy)
        layout.addWidget(copy_btn)

        # Run button
        run_btn = QPushButton("▶")
        run_btn.setFixedSize(QSize(28, 28))
        run_btn.setToolTip("Execute this command and stream output below")
        run_btn.clicked.connect(lambda: self.run_requested.emit(self._command))
        layout.addWidget(run_btn)

    def _copy(self) -> None:
        QApplication.clipboard().setText(self._command)


# ---------------------------------------------------------------------------
# Hypothesis card widget
# ---------------------------------------------------------------------------

class _HypothesisCard(QFrame):
    """Expandable card widget representing a single Hypothesis."""

    exploit_requested     = pyqtSignal(object)  # emits Hypothesis (pwn)
    rsa_exploit_requested = pyqtSignal(object)  # emits Hypothesis (crypto)

    def __init__(self, hyp: Hypothesis, parent: Optional[QWidget] = None) -> None:
        super().__init__(parent)
        self._hyp = hyp
        self._expanded = False

        fg, bg = _category_colours(hyp.category)

        self.setFrameShape(QFrame.Shape.StyledPanel)
        self.setStyleSheet(f"QFrame {{ background:{bg}; border:1px solid #cccccc; border-radius:4px; }}")

        self._outer = QVBoxLayout(self)
        self._outer.setContentsMargins(8, 6, 8, 6)
        self._outer.setSpacing(4)

        # ── Header row ───────────────────────────────────────────────────
        header = QHBoxLayout()
        header.setSpacing(6)

        # Toggle button
        self._toggle_btn = QPushButton("▶")
        self._toggle_btn.setFixedSize(QSize(20, 20))
        self._toggle_btn.setFlat(True)
        self._toggle_btn.clicked.connect(self._toggle)
        header.addWidget(self._toggle_btn)

        # Category badge
        badge = QLabel(hyp.category.upper())
        badge.setFont(QFont("sans-serif", 8, QFont.Weight.Bold))
        badge.setStyleSheet(
            f"background:{fg}; color:white; padding:2px 6px; border-radius:3px;"
        )
        header.addWidget(badge)

        # Title
        title_lbl = QLabel(hyp.title)
        title_font = QFont()
        title_font.setBold(True)
        title_lbl.setFont(title_font)
        title_lbl.setWordWrap(True)
        header.addWidget(title_lbl, 1)

        # Confidence bar
        conf_bar = QProgressBar()
        conf_bar.setRange(0, 100)
        conf_bar.setValue(int(hyp.confidence * 100))
        conf_bar.setFixedWidth(80)
        conf_bar.setFixedHeight(14)
        conf_bar.setTextVisible(True)
        conf_bar.setFormat(f"{hyp.confidence * 100:.0f}%")
        colour = (
            "#cc0000" if hyp.confidence >= 0.85
            else "#cc8800" if hyp.confidence >= 0.65
            else "#2266cc"
        )
        conf_bar.setStyleSheet(
            f"QProgressBar::chunk {{ background:{colour}; }}"
            "QProgressBar { font-size:8px; }"
        )
        header.addWidget(conf_bar)

        # Source tag
        src_icon = "🤖 AI" if hyp.source == "ai" else "📐 Rules"
        src_lbl = QLabel(src_icon)
        src_lbl.setFont(QFont("sans-serif", 8))
        src_lbl.setStyleSheet("color:#666666;")
        header.addWidget(src_lbl)

        self._outer.addLayout(header)

        # ── Collapsible body ─────────────────────────────────────────────
        self._body = QWidget()
        body_layout = QVBoxLayout(self._body)
        body_layout.setContentsMargins(24, 4, 0, 4)
        body_layout.setSpacing(6)

        # Reasoning (if any)
        if hyp.reasoning:
            reason_lbl = QLabel(hyp.reasoning)
            reason_lbl.setWordWrap(True)
            reason_lbl.setStyleSheet("color:#444444; font-style:italic;")
            body_layout.addWidget(reason_lbl)

        # Present findings
        if hyp.present_findings:
            body_layout.addWidget(QLabel("<b>Supporting findings:</b>"))
            for title in hyp.present_findings[:8]:
                row = QLabel(f"  ✅ {title}")
                row.setWordWrap(True)
                body_layout.addWidget(row)

        # Missing findings
        if hyp.missing_findings:
            body_layout.addWidget(QLabel("<b>Still needed to confirm:</b>"))
            for m in hyp.missing_findings:
                row = QLabel(f"  ❓ {m}")
                row.setWordWrap(True)
                body_layout.addWidget(row)

        # Suggested commands
        if hyp.suggested_commands:
            body_layout.addWidget(QLabel("<b>Suggested commands:</b>"))
            for cmd in hyp.suggested_commands:
                block = _CommandBlock(cmd)
                block.run_requested.connect(self._on_run_requested)
                body_layout.addWidget(block)

        # Suggested transforms
        if hyp.suggested_transforms:
            body_layout.addWidget(QLabel("<b>Transform pipeline steps:</b>"))
            steps_lbl = QLabel("  →  ".join(hyp.suggested_transforms))
            steps_lbl.setFont(QFont("Courier", 9))
            steps_lbl.setWordWrap(True)
            body_layout.addWidget(steps_lbl)

        # Exploit generator button (pwn category, confidence ≥ threshold)
        if (
            hyp.category.lower() == "pwn"
            and hyp.confidence >= ExploitGenerator.MIN_CONFIDENCE
        ):
            exploit_btn = QPushButton("🔧 Generate Exploit Script")
            exploit_btn.setToolTip(
                "Generate a pwntools exploit script based on current findings"
            )
            exploit_btn.clicked.connect(lambda: self.exploit_requested.emit(self._hyp))
            body_layout.addWidget(exploit_btn)

        # RSA script generator button (crypto category, confidence ≥ threshold)
        if (
            hyp.category.lower() == "crypto"
            and hyp.confidence >= ExploitGenerator.MIN_CONFIDENCE
        ):
            rsa_btn = QPushButton("🔑 Generate RSA Attack Script")
            rsa_btn.setToolTip(
                "Generate a Python RSA attack script (small-e / factorable-N / common-modulus)"
            )
            rsa_btn.clicked.connect(lambda: self.rsa_exploit_requested.emit(self._hyp))
            body_layout.addWidget(rsa_btn)

        # Command output area (hidden until a Run Command is triggered)
        self._output = QTextEdit()
        self._output.setReadOnly(True)
        self._output.setMaximumHeight(200)
        self._output.setFont(QFont("Courier", 9))
        self._output.setStyleSheet(
            "background:#0d0d0d; color:#e0e0e0; border:none; padding:4px;"
        )
        self._output.hide()
        body_layout.addWidget(self._output)

        self._body.hide()
        self._outer.addWidget(self._body)

        # QProcess for command execution
        self._proc: Optional[QProcess] = None

    # ------------------------------------------------------------------

    def _toggle(self) -> None:
        self._expanded = not self._expanded
        self._body.setVisible(self._expanded)
        self._toggle_btn.setText("▼" if self._expanded else "▶")

    def _on_run_requested(self, command: str) -> None:
        """Execute *command* in a subprocess and stream stdout/stderr to output area."""
        if self._proc and self._proc.state() == QProcess.ProcessState.Running:
            self._proc.kill()

        self._output.clear()
        self._output.show()
        if not self._expanded:
            self._toggle()

        self._proc = QProcess(self)
        self._proc.setProcessChannelMode(QProcess.ProcessChannelMode.MergedChannels)
        self._proc.readyReadStandardOutput.connect(self._on_proc_output)
        self._proc.finished.connect(self._on_proc_finished)

        # Run in a shell so pipes, redirection, etc. work
        self._proc.start("sh", ["-c", command])
        self._output.append(f"$ {command}\n")

    def _on_proc_output(self) -> None:
        data = bytes(self._proc.readAllStandardOutput()).decode("utf-8", errors="replace")
        self._output.moveCursor(QTextCursor.MoveOperation.End)
        self._output.insertPlainText(data)
        self._output.moveCursor(QTextCursor.MoveOperation.End)

    def _on_proc_finished(self, exit_code: int, _status) -> None:
        self._output.append(f"\n[Process exited with code {exit_code}]")


