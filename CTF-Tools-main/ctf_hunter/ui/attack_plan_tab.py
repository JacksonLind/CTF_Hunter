class AttackPlanTab(QWidget):
    """Tab that shows rule-based and AI-generated attack hypotheses as cards."""

    def __init__(self, ai_client=None, parent: Optional[QWidget] = None) -> None:
        super().__init__(parent)
        self._ai_client = ai_client
        self._engine = HypothesisEngine(ai_client=ai_client)
        self._hypotheses: List[Hypothesis] = []
        self._session: Session = Session()
        self._exploit_gen = ExploitGenerator()

        layout = QVBoxLayout(self)
        layout.setContentsMargins(8, 8, 8, 8)
        layout.setSpacing(6)

        # ── Toolbar ──────────────────────────────────────────────────────
        toolbar = QHBoxLayout()
        toolbar.addWidget(QLabel("<b>⚔️ Attack Hypotheses</b> — ordered by confidence"))
        toolbar.addStretch()

        refresh_btn = QPushButton("🔄 Refresh")
        refresh_btn.setToolTip("Re-generate hypotheses from current findings")
        refresh_btn.clicked.connect(self._refresh)
        toolbar.addWidget(refresh_btn)

        layout.addLayout(toolbar)

        # ── Fingerprint banner ────────────────────────────────────────────
        self._fp_banner = _FingerprintBanner()
        layout.addWidget(self._fp_banner)

        # ── Scroll area for hypothesis cards ─────────────────────────────
        self._scroll = QScrollArea()
        self._scroll.setWidgetResizable(True)
        self._scroll.setHorizontalScrollBarPolicy(Qt.ScrollBarPolicy.ScrollBarAlwaysOff)

        self._cards_container = QWidget()
        self._cards_layout = QVBoxLayout(self._cards_container)
        self._cards_layout.setContentsMargins(4, 4, 4, 4)
        self._cards_layout.setSpacing(8)
        self._cards_layout.addStretch()

        self._scroll.setWidget(self._cards_container)
        layout.addWidget(self._scroll, 1)

        # ── Status bar ───────────────────────────────────────────────────
        self._status_lbl = QLabel("No hypotheses yet — load and analyse a file first.")
        self._status_lbl.setStyleSheet("color:#666666; font-style:italic;")
        layout.addWidget(self._status_lbl)

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def set_ai_client(self, ai_client) -> None:
        self._ai_client = ai_client
        self._engine = HypothesisEngine(ai_client=ai_client)

    def update_session(self, session: Session) -> None:
        self._session = session
        self._refresh()

    # ------------------------------------------------------------------

    def _refresh(self) -> None:
        self._hypotheses = self._engine.run(self._session)
        self._rebuild_cards()

    def _rebuild_cards(self) -> None:
        # Remove existing cards (keep the trailing stretch)
        while self._cards_layout.count() > 1:
            item = self._cards_layout.takeAt(0)
            if item.widget():
                item.widget().deleteLater()

        if not self._hypotheses:
            self._fp_banner.clear()
            self._status_lbl.setText(
                "No hypotheses matched — load and analyse a challenge file first."
            )
            return

        # Update fingerprint banner with top fingerprint hypothesis
        fp_hyps = [h for h in self._hypotheses if h.source == "fingerprint"]
        if fp_hyps:
            self._fp_banner.update_match(fp_hyps[0])
        else:
            self._fp_banner.clear()

        self._status_lbl.setText(
            f"{len(self._hypotheses)} hypothesis(es) generated — "
            "click ▶ on any card to expand details."
        )

        for hyp in self._hypotheses:
            card = _HypothesisCard(hyp)
            card.exploit_requested.connect(self._on_exploit_requested)
            card.rsa_exploit_requested.connect(self._on_rsa_exploit_requested)
            # Insert before the trailing stretch
            self._cards_layout.insertWidget(self._cards_layout.count() - 1, card)

    def _on_exploit_requested(self, hyp: Hypothesis) -> None:
        """Generate a pwntools exploit script for the given pwn hypothesis."""
        # Find the binary path from session files (heuristic: first ELF-ish file)
        binary_path: Optional[str] = None
        for fpath in self._session.files:
            ext = os.path.splitext(fpath)[1].lower()
            if ext in ("", ".elf", ".bin", ".out", ".exe") or "elf" in fpath.lower():
                binary_path = fpath
                break
        if binary_path is None and self._session.files:
            binary_path = self._session.files[0]
        if binary_path is None:
            QMessageBox.warning(self, "Exploit Generator", "No binary file found in session.")
            return

        ctx = self._exploit_gen.extract_context(self._session, binary_path)

        # Choose template based on whether it's a format string or overflow hypothesis
        if hyp.category.lower() == "pwn" and ctx.is_format_string:
            script = self._exploit_gen.generate_format_string_exploit(ctx)
            default_name = f"exploit_fmtstr_{os.path.basename(binary_path)}.py"
        else:
            script = self._exploit_gen.generate_rop_exploit(ctx)
            default_name = self._exploit_gen.default_output_path(ctx)

        # Ask user where to save
        save_path, _ = QFileDialog.getSaveFileName(
            self,
            "Save Exploit Script",
            default_name,
            "Python scripts (*.py);;All files (*)",
        )
        if not save_path:
            return

        try:
            with open(save_path, "w") as fh:
                fh.write(script)
            QMessageBox.information(
                self,
                "Exploit Generated",
                f"Exploit script saved to:\n{save_path}\n\n"
                "Update BINARY, OFFSET, REMOTE_HOST/PORT and libc path before running.",
            )
        except OSError as exc:
            QMessageBox.critical(self, "Save Failed", str(exc))

    def _on_rsa_exploit_requested(self, hyp: Hypothesis) -> None:
        """Generate a Python RSA attack script for the given crypto hypothesis."""
        # Build a minimal ExploitContext for RSA — no binary required
        # Map hypothesis title to attack type for the generator
        title_lower = hyp.title.lower()
        if "small" in title_lower and ("e" in title_lower or "exponent" in title_lower):
            attack_type: Optional[str] = "small_e"
        elif re.search(r"\bfactor(ed|able)?\b", title_lower):
            attack_type = "factorable_n"
        elif "common modulus" in title_lower or "common_modulus" in title_lower:
            attack_type = "common_modulus"
        else:
            attack_type = None  # include all three stubs

        ctx = ExploitContext(
            binary_path="challenge",
            rsa_attack_type=attack_type,
        )
        script = self._exploit_gen.generate_rsa_exploit(ctx)
        default_name = self._exploit_gen.default_output_path(ctx)

        save_path, _ = QFileDialog.getSaveFileName(
            self,
            "Save RSA Attack Script",
            default_name,
            "Python scripts (*.py);;All files (*)",
        )
        if not save_path:
            return

        try:
            with open(save_path, "w") as fh:
                fh.write(script)
            QMessageBox.information(
                self,
                "RSA Script Generated",
                f"RSA attack script saved to:\n{save_path}\n\n"
                "Set N, e, c (and p/q or e2/c2 as appropriate) then run:\n"
                f"  python3 {save_path}",
            )
        except OSError as exc:
            QMessageBox.critical(self, "Save Failed", str(exc))
